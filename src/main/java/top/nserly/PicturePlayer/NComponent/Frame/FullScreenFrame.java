    private JPanel panel1;
    private PaintPicturePanel.ImageCanvas imageCanvas;

    public FullScreenFrame() {
        init();
    }

    public FullScreenFrame(PaintPicturePanel.ImageCanvas imageCanvas) {
        init();
        setImageCanvas(imageCanvas);
    }

    private void init() {
        setUndecorated(true);
        setSize(SizeOperate.FreeOfScreenSize);
        getContentPane().add(panel1);
        addComponentListener(new ComponentAdapter() {
            @Override
            public void componentResized(ComponentEvent e) {
                if (!isShowing()) return;
                PaintPicturePanel.paintPicture.sizeOperate.incomeWindowDimension(imageCanvas.getSize());
                PaintPicturePanel.paintPicture.sizeOperate.update(false);
            }
        });
        addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosing(WindowEvent e) {
                PaintPicturePanel.paintPicture.imageCanvas.setFullScreen(false);
            }
        });
    }

    public void setVisible(boolean visible) {
        super.setVisible(visible);
        if (visible && imageCanvas != null) {
            imageCanvas.requestFocusInWindow();
        }
    }

    public void setImageCanvas(PaintPicturePanel.ImageCanvas imageCanvas) {
        if (imageCanvas == null) return;
        if (this.imageCanvas != null)
            panel1.remove(this.imageCanvas);
        this.imageCanvas = imageCanvas;
        panel1.add(imageCanvas, BorderLayout.CENTER);
    }

